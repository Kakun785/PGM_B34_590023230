
#include <stdio.h>
int main (){
    
    int number;

    // Input
    printf("Enter an integer: ");
    scanf("%d", &number);

    // Check even or odd using if-else
    if (number % 2 == 0) {
        printf("%d is Even.\n", number);
    } else {
        printf("%d is Odd.\n", number);
    }

    return 0;
}














   
   



























